""" 정수 3개 입력받아 세 숫자의 다양한 연산 구하기 """

a = int(input('숫자1 입력: '))
b = int(input('숫자2 입력: '))
c = int(input('숫자3 입력: '))

print(a, '+', b, '+', c, '=', a+b+c)
print(a, 'x', b, 'x', c, '=', a*b*c)
print(a, '/', b, '/', c, '=', a/b/c)
print(a, '%', b, '%', c, '=', a%b%c)
print(a, '^', b, '^', c, '=', a^b^c)
